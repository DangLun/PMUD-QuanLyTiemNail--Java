    private void jb_thaydoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_thaydoiActionPerformed
        // TODO add your handling code here:
        LocalDate x = LocalDate.now();
        if (jtennv.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên nhân viên !", "thông báo", JOptionPane.WARNING_MESSAGE);
            jtennv.requestFocus();
        } else if(jsdt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Không được để trống số điện thoại !", "thông báo", JOptionPane.WARNING_MESSAGE);
            jsdt.requestFocus();
        }else if(jngay.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Không được để trống ngày sinh !", "thông báo", JOptionPane.WARNING_MESSAGE);
            jngay.requestFocus();
        }else if(jthang.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Không được để trống tháng sinh !", "thông báo", JOptionPane.WARNING_MESSAGE);
            jthang.requestFocus();
        }else if(jnam.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Không được để trống năm sinh !", "thông báo", JOptionPane.WARNING_MESSAGE);
            jnam.requestFocus();
        }else if(jluong.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Không được để trống lương !", "thông báo", JOptionPane.WARNING_MESSAGE);
            jluong.requestFocus();
        }else if (jsdt.getText().length() != 10) {
            JOptionPane.showMessageDialog(this, "Độ dài số điện thoại không hợp lệ !", "thông báo", JOptionPane.WARNING_MESSAGE);
        }
        else if (check(Integer.parseInt(jngay.getText()), Integer.parseInt(jthang.getText()), Integer.parseInt(jnam.getText())) == false) {
            JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ !", "thông báo", JOptionPane.WARNING_MESSAGE);
        }else if(Integer.parseInt(jnam.getText()) > x.getYear()){
            JOptionPane.showMessageDialog(this, "Mới có năm 2023 đâu ra bà sinh năm lớn hơn được -.- !", "thông báo", JOptionPane.WARNING_MESSAGE);
            jnam.requestFocus();
        }else if(Integer.parseInt(jnam.getText()) == x.getYear() && Integer.parseInt(jthang.getText()) > x.getMonthValue()){
            JOptionPane.showMessageDialog(this, "Mới có tháng 06 sao mà bà sinh lớn hơn được -.- !", "thông báo", JOptionPane.WARNING_MESSAGE);
            jthang.requestFocus();
        }else if(Integer.parseInt(jnam.getText()) == x.getYear() && Integer.parseInt(jthang.getText()) == x.getMonthValue() && Integer.parseInt(jngay.getText()) > x.getDayOfMonth()){
            JOptionPane.showMessageDialog(this, "Không thể nhập ngày lớn hơn ngày hiện tại !", "thông báo", JOptionPane.WARNING_MESSAGE);
            jngay.requestFocus();
        }
        else{
            String gioitinh = "Nam";
            if(jr_nu.isSelected()){
                gioitinh = "Nữ";
            }
            String ngaysinh = jnam.getText() + "-" + jthang.getText() + "-" + jngay.getText();
            try{
                String query = """
                call updateTableNhanVien(?, ?, ?, ?, ?, ?);
                """;
                pst = conn.prepareStatement(query);
                pst.setInt(1, Integer.parseInt(id1));
                pst.setString(2, jtennv.getText());
                pst.setString(3, jsdt.getText());
                pst.setString(5, ngaysinh);
                pst.setString(4, gioitinh);
                pst.setInt(6, Integer.parseInt(jluong.getText()));
                pst.executeQuery();
                JOptionPane.showMessageDialog(this, "Sửa thông tin thành công !");
                jtennv.setText("");
                jsdt.setText("");
                jngay.setText("");
                jthang.setText("");
                jnam.setText("");
                jr_nam.setSelected(true);
                jluong.setText("");
                jtennv.requestFocus();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_jb_thaydoiActionPerformed

