    public SuaThongTinNhanVien(String tennd, String id, String tennv, String sdtnv, String gt, String ns, String luong) {
        initComponents();
        ButtonGroup bg = new ButtonGroup();
        bg.add(jr_nu);
        bg.add(jr_nam);
        jid.setText(id);
        jtennv.setText(tennv);
        jsdt.setText(sdtnv);
        if("Nam".equals(gt)){
            jr_nam.setSelected(true);
        }else{
            jr_nu.setSelected(true);
        }
        String ngay= "", thang = "", nam = "";
        char x[] = ns.toCharArray();
        int y = 0;
        while(x[y] != '-'){
            nam += x[y];
            y++;
        }
        y++;
        while(x[y] != '-'){
            thang += x[y];
            y++;
        }
        y++;
        while(y < x.length){
            ngay += x[y];
            y++;
        }
        jngay.setText(ngay);
        jthang.setText(thang);
        jnam.setText(nam);
        jluong.setText(luong);
        setLocationRelativeTo(null);
    }
